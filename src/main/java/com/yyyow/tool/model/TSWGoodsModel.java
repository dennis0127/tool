package com.yyyow.tool.model;

public class TSWGoodsModel {
    private String name;

    private String brightSpot;

    private String clintName;

    private String lastActiveDate;

    private String areaName;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBrightSpot() {
        return brightSpot;
    }

    public void setBrightSpot(String brightSpot) {
        this.brightSpot = brightSpot;
    }

    public String getClintName() {
        return clintName;
    }

    public void setClintName(String clintName) {
        this.clintName = clintName;
    }

    public String getLastActiveDate() {
        return lastActiveDate;
    }

    public void setLastActiveDate(String lastActiveDate) {
        this.lastActiveDate = lastActiveDate;
    }

    public String getAreaName() {
        return areaName;
    }

    public void setAreaName(String areaName) {
        this.areaName = areaName;
    }
}
